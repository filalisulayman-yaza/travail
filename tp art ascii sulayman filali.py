def afficher_ligne(n):
    for p in range(n):
        print("O", end = "")
        
def afficher_carre(n):
    for p in range(n):
        for x in range(n):
            print("O", end = "")
        print()
        
def afficher_rectangle(y,x):
    for p in range(y):
        for p in range(x):
            print("O", end = "")
        print()
        
def afficher_triangle_rectangle(n):
    nombre = 1
    for p in range(n):
        for x in range(nombre):
            print("O", end = "")
        print()
        nombre = nombre + 1
        
